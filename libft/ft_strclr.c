#include "includes/libft.h"

/*
** sets all values of string to \0
** ft_strclr("Hello") -> "\0\0\0\0\0"
*/

void	ft_strclr(char *s)
{
	if (!s)
		return ;
	ft_strset(s, '\0');
}
