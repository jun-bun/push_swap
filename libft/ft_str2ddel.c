#include "../includes/libft.h"

void    ft_str2ddel(char    ***s)
{
    char    **ptr;

    if (!s || !*s)
        return;

    ptr = *s;
    while(ptr)
    {
        ft_strdel(ptr);
        ptr++;
    }
    free(*s);
}